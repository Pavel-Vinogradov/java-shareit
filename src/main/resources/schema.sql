DROP ALL OBJECTS;

CREATE TABLE IF NOT EXISTS users
(
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(40),
    email VARCHAR(40)
    );

CREATE TABLE IF NOT EXISTS item
(
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(40),
    description VARCHAR(255),
    available BOOLEAN,
    owner INT REFERENCES users (id) ON DELETE CASCADE,
    request INT
    );